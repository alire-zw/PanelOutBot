const PANEL_VOLUME_STATE_PATTERN = "(\\d+)_(\\d+)_(\\d+)";

export function encodePanelVolumeState(count, maxUsers, days) {
  return `${count}_${maxUsers}_${days}`;
}

export function parsePanelVolumeState(value) {
  if (value == null) {
    return null;
  }

  const fullMatch = String(value).match(/^(\d+)_(\d+)_(\d+)$/);

  if (fullMatch) {
    return {
      count: Number(fullMatch[1]),
      maxUsers: Number(fullMatch[2]),
      days: Number(fullMatch[3]),
    };
  }

  const partialMatch = String(value).match(/^(\d+)_(\d+)$/);

  if (partialMatch) {
    return {
      count: Number(partialMatch[1]),
      maxUsers: Number(partialMatch[2]),
      days: null,
    };
  }

  const legacyCount = Number(value);

  if (Number.isFinite(legacyCount) && legacyCount > 0) {
    return { count: legacyCount, maxUsers: null, days: null };
  }

  return null;
}

