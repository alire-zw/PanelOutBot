export function parsePanelVolumeAction(callbackData) {
  if (callbackData === CallbackData.NEW_SERVICE_PANEL_VOLUME) {
    return { action: "view", gb: null, maxUsers: null, days: null };
  }

  if (callbackData === CallbackData.NEW_SERVICE_PANEL_VOLUME_DISPLAY) {
    return { action: "display", gb: null, maxUsers: null, days: null };
  }

  if (callbackData === CallbackData.NEW_SERVICE_PANEL_VOLUME_USERS_DISPLAY) {
    return { action: "users_display", gb: null, maxUsers: null, days: null };
  }

  if (callbackData === CallbackData.NEW_SERVICE_PANEL_VOLUME_DAYS_DISPLAY) {
    return { action: "days_display", gb: null, maxUsers: null, days: null };
  }

  const incAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:inc:",
    "inc",
  );
  if (incAction) {
    return incAction;
  }

  const decAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:dec:",
    "dec",
  );
  if (decAction) {
    return decAction;
  }

  const usersIncAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:users_inc:",
    "users_inc",
  );
  if (usersIncAction) {
    return usersIncAction;
  }

  const usersDecAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:users_dec:",
    "users_dec",
  );
  if (usersDecAction) {
    return usersDecAction;
  }

  const daysIncAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:days_inc:",
    "days_inc",
  );
  if (daysIncAction) {
    return daysIncAction;
  }

  const daysDecAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:days_dec:",
    "days_dec",
  );
  if (daysDecAction) {
    return daysDecAction;
  }

  const continueAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:continue:",
    "continue",
  );
  if (continueAction) {
    return continueAction;
  }

  const atAction = parsePanelVolumeStateAction(
    callbackData,
    "^menu:new_service:panel:volume:at:",
    "view",
  );
  if (atAction) {
    return atAction;
  }

  return null;
}

